package week02;

public class Prac108 {   //Switch
    public static void main(String[] args) {
        int i = 20;
        switch(i) {
            case 10:
                System.out.println("Entered Value is: " + i);
            case 20:
                System.out.println("Entered Value is: " + i);
            default:
                System.out.println("Not in Range");
        }
    }
}